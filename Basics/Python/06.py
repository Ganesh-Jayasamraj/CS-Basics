# Types of python operators

'''
Arithmetic Operators
Comparision Operators
Assignment Operators
Logical Operators
Bitwise Operators
Membership Operators
Identity Operators
'''


# Arithematic Operator
print(10 + 20) 
print(4**3)     # Exponent
print(33 % 4)   # returns remainder - Modulus
print(10//4)    # returns quotient - Floor Division

#  Comparision Operators
print(5==5)     # returns True
print(5==6)     # returns False
print(5!=6)     # returns True

# Assignment Operator
a = 10
a+=6            # a = a + 6
print( a )

# Bitwise Operators
a = 60
b = 13

print(a&b)      # AND
print(a|b)      # OR
print(a^b)      # XOR
print(~a)       # Complement
print(a<<2)     # Left Shift
print(a>>2)     # Right Shift